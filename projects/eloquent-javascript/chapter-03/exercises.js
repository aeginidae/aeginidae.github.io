// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

const min = (x, y) => x > y ? y : x;

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
    // given a whole number, recursively determines evenness
    // returns true if even, else false
    x = Math.abs(x);
    if (x === 0) {
        return true;
    }
    else if (x === 1) {
        return false;
    }
    else {
        return isEven(x - 2);
    }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


function countChars(char) {
  let re = new RegExp(char, "g");
  return (str) => re.test(str) ? str.match(re).length : 0;   // returns the length of the array
                                          // containing every instance of char
}

function countChar(str, char) {
  let re = new RegExp(char, "g");
  return re.test(str) ? str.match(re).length : 0;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

const countBs = countChars('B');

